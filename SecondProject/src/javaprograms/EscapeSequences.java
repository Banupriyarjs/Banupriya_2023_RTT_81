package javaprograms;

public class EscapeSequences {

	public static void main(String[] args) {
		//tab
		System.out.println("This is a tab character ->\t<-");
		
		//Basic formatting with tabs
		System.out.println("1\tOne\t\tCol1");
		System.out.println("2\tTwo\t\tCol2");
		System.out.println("3\tThree\t\tCol3");
		System.out.println("4\tFour\t\tCol4");
		System.out.println("100\tOne Hundred\t\tCol5");
		
		//newline
		System.out.println("This is line1.\nThis is line 2");
		
		//to print \
		System.out.println("To get a slash to print we need both of them \\");
		
		//backspace character 
		System.out.println("This is for \b backspace character \b");
		
		//carriage return character 
		System.out.println("This is an example of \r carriage return");
		
		// to print quotes
		System.out.println("\"Hardwork always pays off\"");
		
		
		// to print single quotes '
		System.out.println("Hi \'Parker\'");
		
		// to print backslashes \\
		System.out.println("This will print two back-slashes:\\\\");
		
		// Form feed character
			System.out.println("Form feed\f character.");
				
		

			}

	}


